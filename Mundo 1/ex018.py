from math import sin,cos,tan

ang = float(input('\nDigite o ângulo que você deseja: '))

seno = sin(ang)
cosseno = cos(ang)
tangente = tan(ang)

print(f'\nO ângulo de {ang} tem o SENO de {seno}')
print(f'\nO ângulo de {ang} tem o COSSENO de {cosseno}')
print(f'\nO ângulo de {ang} tem o TANGENTE de {tangente}')