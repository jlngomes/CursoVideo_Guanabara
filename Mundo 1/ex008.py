medida = int(input("\nDigite um valor e veja suas variações: "))

km = medida / 1000
print(f'\n{km} km')

hm = medida / 100
print(f'\n{hm} hm')

dam = medida / 10
print(f'\n{dam} dam')

dm = medida * 10
print(f'\n{dm} dm')

cm = medida * 100
print(f'\n{cm} cm')

mm = medida * 1000
print(f'\n{mm} mm')